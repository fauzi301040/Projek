                        halus yang tampak seperti salju. Itulah sebabnya kue ini dikenal dengan istilah putri salju. Sementara adonan dasarnya terdiri 
                        atas tepung terigu, tepung maizena, mentega dan kuning telur. Kue jenis ini banyak digemari karena rasanya yang gurih sekaligus manis.</p>
                    </div>
                <h2>Cara membuat Putri Salju</h2>
                <div class="card-cookies">
                    <div class="card-content">
                        <h3>~Alat-alat~ :</h3>
                        <ol>
                            <li>Parutan Keju</li>  
                            <li>Wadah</li>
                            <li>Cetakan Bulan Sabit</li>
                            <li>Oven</li>
                            <li>Toples</li>
                        </ol>
                        
                        <h3>~Bahan-bahan~ :</h3>
                        <ol>
                            <li>450 gram tepung terigu (kunci biru)</li>
                            <li>50 gram tepung maizena</li>
                            <li>200 gram margarin (100 margarin/100 gram butter)</li>
                            <li>100 gram gula halus</li>
                            <li>100 gram keju parut</li>
                            <li>1 sdt garam</li>
                            <li>2 butir kuning telur</li>
                        </ol>
                    </div>
                    
                    <div class="card-content">
                        <h3>~Langkah-langkah~ :</h3>
                            <span>Pertama :</span>
                            <p>Campur tepung-tepung dan gula halus aduk rata beri garam aduk lagi sampai rata lalu masukan mentega aduk lagi.</p>
                            
                            <span>Ke-dua :</span>
                            <p>Beri kocokan telur kuningnya dan keju cheddar parut aduk sampai tidak lengket ditangan dan bisa dibentuk.</p>
                            
                            <span>Ke-tiga :</span>
                            <p>Pipihkan adonan dengan ketebalan 3 mm, cetak menggunakan cetakan bulat sabit.</p>
                            
                            <span>Ke-empat :</span>
                            <p>Panggang dengan suhu 160â¢c selama 20 menit atau seauaikan dengan oven masing-masing.</p>
                        </div>
                    <div class="card-content">
                        <br>
                        <br>
                            <span>Ke-lima :</span>
                            <p>Setelah matang,angkat,beri gula halus, baluri semua kue sampai rata.</p>
                            
                            <span>Ke-enam :</span>
                            <p>Tata cantik di toples dan siap disajikan.</p>
