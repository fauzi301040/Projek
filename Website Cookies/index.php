                <h2>Apa itu Nastar ?</h2>
                    <div class="history">
                    <img src="./image/nastar.jpg" alt="Nastar">
                    <p class="card">&nbsp;&nbsp;&nbsp;&nbsp;Nastar adalah jenis kue kering yang sangat populer disajikan dalam acara-acara besar. Siapa sih yang tidak suka nastar ?
                        Kue kering ini terbuat dari adonan tepung terigu, mentega, dan telur. Di dalamnya diisi dengan selai nanas, bentuknya bulat dengan diameter 2 cm. Seringkali 
                        diatasnya ada hiasan berupa parutan keju atau potongan kismis. Nastar sendiri berasal dari bahasa Belanda yaitu "ananas" dan "taart"</p>
                    </div>
                <h2>Cara membuat Nastar</h2>
                <div class="card-cookies">
                    <div class="card-content">
                        <h3>~Alat-alat~ :</h3>
                        <ol>
                            <li>Parutan</li>  
                            <li>Wadah</li>
                            <li>Oven</li>
                            <li>Toples</li>
                        </ol>
                        
                        <h3>~Bahan-bahan~ :</h3>
                        <ol>
                            <li>1 kg Tepung Terigu</li>
                            <li>3 sacet mentega(200 gram 1 sacet)</li>
                            <li>1 bungkus gula halus (100 gram)</li>
                            <li>8 butir telur (kuningnya saja)</li>
                            <li>2 sdm susu bubuk</li>
                            <li>2 sdm rum buttert</li>
                            <li>1 sacet vanili</li>
                            <li>2 butir telur bebek (untuk olesan,kuningnya saja)</li>
                            <li>4 buah nanas</li>
                            <li>2 blok keju parut</li>
                        </ol>

                        <h3>~Topping~ :</h3>
                            <p>Keju parutan</p>
                            <p>Choco chips</p>
                    </div>
                    
                    <div class="card-content">
                        <h3>~Langkah-langkah~ :</h3>
                            <span>Pertama :</span>
                            <p>Buat selai nanas, parut nanas lalu masak hingga kering. Angkat sisihkan.</p>
                            
                            <span>Ke-dua :</span>
                            <p>Campurkan mentega, gula halus, kuning telur, rum butter, susu bubuk, vanili. Aduk hingga tercampur rata.</p>
                            
                            <span>Ke-tiga :</span>
                            <p>Jika sudah tercampur rata masukan terigu sedikit demi sedikit hingga adonan tidak lengket di tangan.</p>
                            
                            <span>Ke-empat :</span>
                            <p>Ambil bulatan kecil lalu pipihkan dan masukan selai nanas, bulatkan kembali sampai adonan habis.</p>
                            
                        </div>
                    <div class="card-content">
                        <br>
                        <br>
                            <span>Ke-lima :</span>
                            <p>Oleskan atasnya dengan kuning telur bebek. Taburi keju di atasnya.</p>
                            
                            <span>Ke-enam :</span>
                            <p>Kemudian oven selama 15 menit smp adonan kering. Dan sajikan ..Nastar yg lembut siap di nikmati 4 toples.</p>
